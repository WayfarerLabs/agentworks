"""Tmuxinator config generation for workspace consoles.

Agentworks owns the workspace .tmuxinator.yml file. It is regenerated from DB
state on any structural change (task create/delete/restart, agent
create/delete). Manual edits will be overwritten.

The generated config produces a workspace console: an admin-shell window plus
one window per task. Task windows attach to the task's locked-down tmux
session using the same wrapper pattern as the VM console.
"""

from __future__ import annotations

import shlex
from typing import TYPE_CHECKING

from agentworks.tasks.tmux import derive_session_name

if TYPE_CHECKING:
    from agentworks.db import TaskRow

GENERATED_HEADER = "# Generated by agentworks. Changes will be overwritten."


def console_session_name(ws_name: str) -> str:
    """Return the tmux session name for a workspace console."""
    return f"ws-{ws_name}-console"


def generate_config(
    ws_name: str,
    workspace_path: str,
    tasks: list[TaskRow] | None = None,
) -> str:
    """Generate a tmuxinator YAML config for a workspace.

    Produces an admin-shell window plus one window per task. Task windows
    use a wrapper that attaches to the task's locked-down tmux session.
    """
    lines = [
        GENERATED_HEADER,
        f"name: {console_session_name(ws_name)}",
        f"root: {workspace_path}",
        "on_project_start: tmux set remain-on-exit on",
        "windows:",
        "  - admin-shell:",
        "      panes:",
        '        - ""',
    ]

    for task in tasks or []:
        session = derive_session_name(ws_name, task.name)
        q_session = shlex.quote(session)
        # Wrapper: unset TMUX for nesting, loop attach while session exists
        wrapper = (
            f"unset TMUX; "
            f"while tmux has-session -t {q_session} 2>/dev/null; do "
            f"tmux attach -t {q_session}; "
            f"sleep 0.5; "
            f"done; "
            f"echo 'Task session {q_session} has ended. Press enter to close.'; "
            f"read"
        )
        lines.extend(
            [
                f"  - {session}:",
                "      panes:",
                f"        - {wrapper}",
            ]
        )

    lines.append("")  # trailing newline
    return "\n".join(lines)
